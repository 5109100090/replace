package com.amca.android.replace.model;

public class User {
	private Integer userId;
	private String userName;
	private String userAlias;
	private String userPassword;
	private String userFoods;
	private String userDrinks;
	private String userBooks;
	private String userMovies;
	private String userGender;
	private String userOccupation;
	private String userDOB;

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getUserAlias() {
		return userAlias;
	}

	public void setUserAlias(String userAlias) {
		this.userAlias = userAlias;
	}

	public String getUserPassword() {
		return userPassword;
	}

	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}

	public String getUserFoods() {
		return userFoods;
	}

	public void setUserFoods(String userFoods) {
		this.userFoods = userFoods;
	}

	public String getUserDrinks() {
		return userDrinks;
	}

	public void setUserDrinks(String userDrinks) {
		this.userDrinks = userDrinks;
	}

	public String getUserBooks() {
		return userBooks;
	}

	public void setUserBooks(String userBooks) {
		this.userBooks = userBooks;
	}

	public String getUserMovies() {
		return userMovies;
	}

	public void setUserMovies(String userMovies) {
		this.userMovies = userMovies;
	}

	public String getUserGender() {
		return userGender;
	}

	public void setUserGender(String userGender) {
		this.userGender = userGender;
	}

	public String getUserOccupation() {
		return userOccupation;
	}

	public void setUserOccupation(String userOccupation) {
		this.userOccupation = userOccupation;
	}

	public String getUserDOB() {
		return userDOB;
	}

	public void setUserDOB(String userDOB) {
		this.userDOB = userDOB;
	}

}
